name = "Shubham"
print(name[0:3]) # shu

print(name[-4: -1]) #bha

print(name[1:4]) # hub

print(name[:4]) # is same as print(name[0:4])
print(name[1:]) # is same as print(name[1:5])
print(name[1:5])

